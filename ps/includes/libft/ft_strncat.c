#include <string.h>

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*c;
	size_t	k;

	c = s1;
	k = 0;
	while (*s1 != '\0')
		s1++;
	while (k < n && *s2 != '\0')
	{
		*s1 = *s2;
		s1++;
		s2++;
		k++;
	}
	*s1 = '\0';
	return (c);
}
