long int	razryad(long int n)
{
	long int	k;

	k = 1;
	if (n < 0)
		n *= -1;
	if (n == 0)
		k = 10;
	while (n > 0)
	{
		k *= 10;
		n /= 10;
	}
	return (k);
}
