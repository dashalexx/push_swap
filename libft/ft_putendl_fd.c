#include <unistd.h>

void	ft_putendl_fd(char const *s, int fd)
{
	if (fd >= 0 && s)
	{
		while (*s != '\0')
		{
			write(fd, s, 1);
			s++;
		}
		write(fd, "\n", 1);
	}
}
